def xor_data(data, key):
    key_bytes = key.encode('utf-8')
    return bytes([b ^ key_bytes[i % len(key_bytes)] for i, b in enumerate(data)])

class PawFS_Builder:
    def __init__(self, size_mb=10):
        self.image_size = size_mb * 1024 * 1024
        self.image_data = bytearray(random.choices(range(256), k=self.image_size))
        self.occupied_blocks = set()

    def find_free_offset(self, start_search=0):
        offset = start_search
        while True:
            block_idx = offset // BLOCK_SIZE
            if block_idx not in self.occupied_blocks:
                return offset
            offset += BLOCK_SIZE
    
    def add_file(self, filename, content, start_offset):
        print(f"[*] Adding file '{filename}' to the image...")
        content_chunks = [content[i:i+DATA_AREA_SIZE] for i in range(0, len(content), DATA_AREA_SIZE)]
        
        current_offset = start_offset
        last_block_offset = 0
        
        for i, chunk in enumerate(content_chunks):
            block_offset = self.find_free_offset(current_offset)
            block_idx = block_offset // BLOCK_SIZE
            self.occupied_blocks.add(block_idx)
            
            if i > 0:
                next_ptr_packed = struct.pack('<I', block_offset)
                self.image_data[last_block_offset+24 : last_block_offset+28] = next_ptr_packed
            
            next_ptr = 0 if (i == len(content_chunks) - 1) else 1
            
            filename_bytes = filename.encode('utf-8').ljust(FILENAME_LEN, b'\x00')
            header = struct.pack('<I16sII', MAGIC_NUMBER, filename_bytes, len(chunk), next_ptr)
            
            obfuscated_chunk = xor_data(chunk, filename)
            
            self.image_data[block_offset : block_offset+len(header)] = header
            self.image_data[block_offset+len(header) : block_offset+len(header)+len(obfuscated_chunk)] = obfuscated_chunk
            
            last_block_offset = block_offset
        
        print(f"    '{filename}' written across {len(content_chunks)} fragment(s).")
    
    def save_image(self, filename="cat_drive.img"):
        with open(filename, 'wb') as f:
            f.write(self.image_data)
        print(f"\n[+] Success! Disk image '{filename}' created.")

